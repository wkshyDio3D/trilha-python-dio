
# ---------- Comando [] Reverse ----------------

# Utilizado para fazer o espelhamento da lista de forma Reversa.
 
linguagens = ["python", "js", "c", "java", "csharp"]
print(linguagens)   # Normal 
# >>> ['python', 'js', 'c', 'java', 'csharp']


linguagens.reverse()
print(linguagens)   # Reverso
# >>> ['csharp', 'java', 'c', 'js', 'python']