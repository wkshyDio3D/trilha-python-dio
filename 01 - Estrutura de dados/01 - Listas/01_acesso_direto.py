
# --- Acessando Dados da Lista por Acesso Direto ----

frutas = ["maçã", "laranja", "uva", "pera"]

print(frutas[0])  # maçã ==> esta na posicao ==>> 0
print(frutas[2])  # uva ==> esta na posicao ==>> 2 

print("")

legumes = ["Batata", "Cenoura", "Beterraba", "Aimpin"]

print(legumes[1])  # Cenoura ==> esta na posicao ==>> 1 
print(legumes[3])  # Aimpin ==> esta na posicao ==>> 3 

print("")

carros = ["Ferrari", "Lamborgine", "Ford", "Fiat", "GMC"]

print(carros[4])    # GMC ==> esta na posicao ==>> 4 
print(carros[1])    # Lamborgine ==> esta na posicao ==>> 1 
print(carros[2])    # Ford ==> esta na posicao ==>> 2

print("")
