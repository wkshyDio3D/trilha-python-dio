
# O ========   comando [] clear   ==========

# Utilizado quendo eu quero limpar a minha lista.

# Exemplo 1 => Saida sem Usar o comando "Clear".
lista = [1, "Python", [40, 30, 20]]

print(lista)  # [1, "Python", [40, 30, 20]]

# lista.clear()

print(lista)  # []

# ------------Saida-------------
# >>> [1, "Python", [40, 30, 20]]
# >>> [1, "Python", [40, 30, 20]]


# ============================================
# Exemplo 2 => Saida Usando o comando "Clear".
lista = [1, "Python", [40, 30, 20]]

print(lista)
# >>> [1, "Python", [40, 30, 20]]


lista.clear()       # Usando o comando Clear  para limpar completamente alista.

print(lista)  # []   # Aqui a lista esta vazia.

# ------------Saida-------------
# >>> [1, "Python", [40, 30, 20]]
# >>> []
