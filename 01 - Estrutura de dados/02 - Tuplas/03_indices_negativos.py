
# ------ Indice Negativos -----------

#  Podemos tambem acessar seus dados utilizando "indices Negativos".
#  Contamos o indice de de determinada sequencia a partir do zero.
# Identico a lista Usase o couchete [-x] onde -x e valor do "indice Negativo" direto.

# Indices=>  0  -     3    -   2  -    1  .
frutas = ("maçã", "laranja", "uva", "pera",)

print(frutas[-1])  # pera
print(frutas[-3])  # laranja
print(frutas[-0])  # maca