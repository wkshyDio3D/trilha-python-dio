
# Metodos da classe "set" ===>> {in}

# Serve para Verificar ou confirmar se um elemento esta
#  dentro da minha conjunto "set".

numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

print(1 in numeros)  # Aqui confirma que o "1" esta no conjunto.
# >>> True
print(10 in numeros) # Aqui confirma que o "10" nao esta no conjunto.
# >>> False
